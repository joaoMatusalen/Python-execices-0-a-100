f = input('Digite algo: ')

print('ele é um número: ', f.isnumeric())
print('ele é alfábetico: ', f.isalpha())
print('ele é um spaço: ', f.isspace())
print('ele está em maiúsculo: ', f.isupper())
print('ele está em minúsculo: ', f.islower())
print('ele é um número e alfábetico: ', f.isalnum())
